void cursor_draw(int x, int y) {
    for(int i = 0; i < 16; i++) {
        for(int j = 0; j < 16; j++) {
            if(cursor_sprite[i][j] == 1) {
                video_pixel(x + j, y + i, 0xFFFFFF);
            } else if(cursor_sprite[i][j] == 2) {
                video_pixel(x + j, y + i, 0x000000);
            }
        }
    }
}
