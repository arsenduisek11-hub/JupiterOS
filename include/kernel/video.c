void video_clear(uint32_t color) {
    for(int i = 0; i < SCREEN_W * SCREEN_H; i++)
        fb[i] = color;
}

void video_pixel(int x, int y, uint32_t color) {
    if(x >= 0 && x < SCREEN_W && y >= 0 && y < SCREEN_H)
        fb[y * SCREEN_W + x] = color;
}

void video_rect(int x, int y, int w, int h, uint32_t color) {
    for(int i = 0; i < w; i++)
        for(int j = 0; j < h; j++)
            video_pixel(x + i, y + j, color);
}

void video_rect_outline(int x, int y, int w, int h, uint32_t color) {
    for(int i = 0; i < w; i++) {
        video_pixel(x + i, y, color);
        video_pixel(x + i, y + h - 1, color);
    }
    for(int i = 0; i < h; i++) {
        video_pixel(x, y + i, color);
        video_pixel(x + w - 1, y + i, color);
    }
}

void video_draw_char(char c, int x, int y, uint32_t color) {
    if(c < 32 || c > 126) return;
    const uint8_t* glyph = font[c - 32];
    for(int i = 0; i < 8; i++) {
        uint8_t row = glyph[i];
        for(int j = 0; j < 8; j++) {
            if(row & (1 << j))
                video_pixel(x + j, y + i, color);
        }
    }
}

void video_draw_str(char* s, int x, int y, uint32_t color) {
    int off = 0;
    while(*s) {
        video_draw_char(*s++, x + off, y, color);
        off += 8;
    }
}
